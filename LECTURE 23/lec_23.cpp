vector<int> preorder(TreeNode* root){
    if(root==NULL)  return {};
    vector<int> result;
    stack <TreeNode*> s;   // create a stack for storing nodes during recursion
    s.push(root);
    TreeNode *temp=root;
    while(true){
        temp=s.top();
        while(temp!=NULL && temp->left!=NULL){
            result.push_back(temp->val);
            s.push(temp);
            temp=temp->left;
        }
        if(s.empty()){
            break;
        }
        temp=s.top();
        s,pop();
        temp=temp->right;
    }
    return result;
}

